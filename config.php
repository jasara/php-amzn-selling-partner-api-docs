<?php

use Illuminate\Support\Str;

return [
    'baseUrl' => 'http://php-amzn-selling-partner-api.test',
    'production' => false,
    'siteName' => 'PHP Selling Partner API Fluent SDK',
    'siteDescription' => 'A PHP package to integrate with the Amazon Selling Partner API with a fluent interface.',

    // Algolia DocSearch credentials
    'docsearchApiKey' => 'test',
    'docsearchIndexName' => 'test',

    // navigation menu
    'navigation' => require_once('navigation.php'),

    // helpers
    'isActive' => function ($page, $path) {
        return Str::endsWith(trimPath($page->getPath()), trimPath($path));
    },
    'isActiveParent' => function ($page, $menuItem) {
        if (is_object($menuItem) && $menuItem->children) {
            return $menuItem->children->contains(function ($child) use ($page) {
                return trimPath($page->getPath()) == trimPath($child);
            });
        }
    },
    'url' => function ($page, $path) {
        return Str::startsWith($path, 'http') ? $path : '/' . trimPath($path);
    },
];
